# ── Shared LLM factory ────────────────────────────────────────────────────────

def get_llm(api_key: Optional[str] = None) -> ChatMistralAI:
    """
    FLAW FIXED: original had a hardcoded plaintext API key as a fallback.
    Now we only accept the key from the environment or caller — never from
    source code.
    """
    key = api_key or os.getenv("MISTRAL_API_KEY")
    if not key:
        raise ValueError(
            "MISTRAL_API_KEY not found. Set it in your .env file or pass it "
            "via the Streamlit sidebar."
        )
    return ChatMistralAI(model="mistral-small-2506", temperature=0, api_key=key)


# ── Self-reflection helper ────────────────────────────────────────────────────

def _reflect(llm: ChatMistralAI, output_json: str, agent_name: str) -> tuple[bool, str]:
    """
    Ask the LLM to score its own output.  Returns (accept: bool, critique: str).
    This is the core of the retry loop — if the agent scores itself below
    REFLECT_THRESHOLD it re-runs with the critique injected.
    """
    prompt = (
        f"You are a strict quality reviewer for the {agent_name}.\n"
        f"Review the following JSON output and score it from 1-10 for completeness, "
        f"accuracy, and usefulness.\n\n"
        f"OUTPUT:\n{output_json}\n\n"
        f"Respond ONLY with a JSON object: "
        f'{{ "score": <int>, "critique": "<one paragraph of specific issues>" }}\n'
        f"No markdown, no preamble."
    )
    try:
        response = llm.invoke([HumanMessage(content=prompt)])
        data = json.loads(response.content.strip())
        score = int(data.get("score", 0))
        critique = data.get("critique", "")
        accept = score >= REFLECT_THRESHOLD
        logger.info(f"[{agent_name}] reflection score={score} accept={accept}")
        return accept, critique
    except Exception as e:
        logger.warning(f"[{agent_name}] reflection parsing failed: {e} — accepting output")
        return True, ""


# ── Agent runner helper ─────────────────────
def _sanitize_response_tool_calls(response: Any, seen_ids: set) -> Any:
    """
    Sanitizes the tool call IDs in a newly received AIMessage.
    Ensures each tool call ID is unique and doesn't conflict with any IDs
    already seen in this conversation.
    """
    import uuid
    if hasattr(response, "tool_calls") and response.tool_calls:
        raw_tool_calls = response.additional_kwargs.get("tool_calls", [])
        
        for idx, tc in enumerate(response.tool_calls):
            orig_id = tc.get("id")
            
            # If the ID is duplicate or empty, generate a new unique one
            if not orig_id or orig_id in seen_ids:
                new_id = f"call_{uuid.uuid4().hex[:8]}"
                tc["id"] = new_id
                
                # Sync with the raw tool calls in additional_kwargs
                if idx < len(raw_tool_calls):
                    tc_raw = raw_tool_calls[idx]
                    if isinstance(tc_raw, dict):
                        tc_raw["id"] = new_id
                    elif hasattr(tc_raw, "id"):
                        try:
                            setattr(tc_raw, "id", new_id)
                        except Exception:
                            pass
                seen_ids.add(new_id)
            else:
                seen_ids.add(orig_id)
                
    return response


def _run_react_agent(
    llm: ChatMistralAI,
    tools: list,
    system_prompt: str,
    user_message: str,
    agent_name: str,
) -> str:
    """
    Runs a custom ReAct loop that executes tools and feeds results back to the LLM.
    Guards against "Duplicate tool call id in assistant message" errors by sanitizing
    new tool call IDs immediately after LLM invocation.
    """
    from langchain_core.messages import ToolMessage
    
    llm_with_tools = llm.bind_tools(tools) if tools else llm
    
    messages = [
        SystemMessage(content=system_prompt),
        HumanMessage(content=user_message),
    ]
    
    tool_map = {t.name: t for t in tools}
    seen_ids = set()
    max_steps = 10
    
    for step in range(max_steps):
        logger.info(f"[{agent_name}] Step {step+1}: invoking LLM...")
        
        # Invoke LLM
        response = llm_with_tools.invoke(messages)
        
        # Sanitize tool calls in this response to prevent duplicate tool call IDs
        response = _sanitize_response_tool_calls(response, seen_ids)
                
        # Add response to history
        print("\n===== MESSAGE HISTORY =====")

        for i, msg in enumerate(messages):
            print(f"\nMessage {i}")
            print(type(msg))

            if hasattr(msg, "tool_calls"):
                print("tool_calls =", msg.tool_calls)
        messages.append(response)
        
        # Check if LLM requested tools
        if not hasattr(response, "tool_calls") or not response.tool_calls:
            logger.info(f"[{agent_name}] LLM completed on step {step+1}")
            return response.content
            
        logger.info(f"[{agent_name}] LLM requested tools: {[tc['name'] for tc in response.tool_calls]}")
        
        # Execute tools
        print("\n===== TOOL EXECUTION =====")
        print("Tool Calls Count:", len(response.tool_calls))
        for tc in response.tool_calls:
            t_name = tc["name"]
            t_args = tc["args"]
            t_id = tc["id"]
            print("Executing:", t_name)
            print("Tool ID:", t_id)
            if t_name in tool_map:
                try:
                    logger.info(f"[{agent_name}] Executing tool '{t_name}' with args {t_args}")
                    tool_result = tool_map[t_name].invoke(t_args)
                except Exception as e:
                    tool_result = f"Error executing tool '{t_name}': {e}"
            else:
                tool_result = f"Error: Tool '{t_name}' is not available."
                
            messages.append(ToolMessage(content=str(tool_result), tool_call_id=t_id))
            
    logger.warning(f"[{agent_name}] Reached max ReAct steps ({max_steps}) without completion.")
    return messages[-1].content



# ── Agent 1: Resume Analyzer ──────────────────────────────────────────────────

def run_resume_analyzer(resume_text: str, api_key: Optional[str] = None) -> ResumeAnalysis:
    """
    Analyzes the candidate's resume.  Can call web_search / verify_skill_definition
    to look up unfamiliar technologies before reporting them.
    Retries up to MAX_RETRIES times if self-reflection score is too low.
    """
    llm = get_llm(api_key)
    system = (
        "You are an expert Resume Analyzer Agent.\n"
        "Extract ALL relevant details from the resume.\n"
        "If you encounter a skill or technology you are not certain about, "
        "use the verify_skill_definition tool before including it.\n"
        "If the resume mentions a company or project that needs context, "
        "use web_search to gather it.\n"
        "Return your final answer as a JSON object matching the ResumeAnalysis schema:\n"
        "candidate_name, email, education (list), experience_years (float), "
        "skills (list), projects (list), strengths (list), weaknesses (list).\n"
        "Output ONLY valid JSON. No markdown fences, no preamble."
    )
    user_msg = f"Analyze this resume:\n\n{resume_text}"

    for attempt in range(MAX_RETRIES + 1):
        raw = _run_react_agent(llm, RESUME_TOOLS, system, user_msg, "ResumeAnalyzer")
        accept, critique = _reflect(llm, raw, "ResumeAnalyzer")
        if accept:
            break
        logger.info(f"[ResumeAnalyzer] retry {attempt+1}: {critique}")
        user_msg = (
            f"Previous attempt was rejected with this critique:\n{critique}\n\n"
            f"Please redo the analysis, fixing the identified issues.\n\n"
            f"Original resume:\n{resume_text}"
        )

    # Parse JSON → Pydantic
    try:
        clean = raw.strip().lstrip("```json").lstrip("```").rstrip("```").strip()
        return ResumeAnalysis.model_validate_json(clean)
    except Exception as e:
        logger.error(f"[ResumeAnalyzer] JSON parse failed: {e}\nRaw: {raw[:300]}")
        raise ValueError(f"ResumeAnalyzer produced invalid JSON: {e}")


