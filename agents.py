# ── Agent 5: Performance Evaluator ────────────────────────────────────────────

def run_performance_evaluator(
    questions: List[InterviewQuestion],
    user_answers: dict,
    api_key: Optional[str] = None,
) -> PerformanceEvaluation:
    """
    Evaluates candidate answers with self-reflection.
    FLAW FIXED (original): original evaluator ran once with no feedback loop.
    Here the evaluator scores itself: if its own quality score is below
    REFLECT_THRESHOLD it re-evaluates, making the feedback more constructive
    and less likely to miss important points.
    """
    llm = get_llm(api_key)

    qa_blocks = []
    for q in questions:
        ans = user_answers.get(str(q.question_id)) or "No answer provided."
        qa_blocks.append(
            f"Q{q.question_id} [{q.question_type}]: {q.question_text}\n"
            f"Expected key points: {', '.join(q.expected_key_points)}\n"
            f"Model answer: {q.model_answer}\n"
            f"Candidate answer: {ans}"
        )
    qa_text = "\n\n---\n\n".join(qa_blocks)

    system = (
        "You are an expert Performance Evaluator Agent.\n"
        "Evaluate each candidate answer against the question, expected key points, "
        "and model answer.  Score each out of 10.  Be honest but constructive.\n"
        "Compute overall_readiness_score (0-100) as a weighted average.\n"
        "Return JSON matching PerformanceEvaluation schema:\n"
        "overall_readiness_score (int), evaluations (list of AnswerEvaluation), "
        "key_strengths (list), key_development_areas (list), final_verdict (str), "
        "reflection_cycles (int, starts at 0).\n"
        "Each AnswerEvaluation: question_id, question_text, user_answer, score (int), "
        "strengths (list), weaknesses (list), suggested_improvement (str).\n"
        "Output ONLY valid JSON. No markdown fences, no preamble."
    )
    user_msg = f"Evaluate the following interview Q&A:\n\n{qa_text}"

    reflection_cycles = 0
    for attempt in range(MAX_RETRIES + 1):
        raw = _run_react_agent(llm, EVALUATOR_TOOLS, system, user_msg, "PerformanceEvaluator")
        accept, critique = _reflect(llm, raw, "PerformanceEvaluator")
        if accept:
            break
        reflection_cycles += 1
        user_msg = (
            f"Your previous evaluation was found lacking:\n{critique}\n\n"
            f"Please re-evaluate more carefully.\n\nOriginal Q&A:\n{qa_text}"
        )

    try:
        import json
        import re

        clean = raw.strip().lstrip("```json").lstrip("```").rstrip("```").strip()
        data = json.loads(clean)
        for item in data.get("evaluations", []):
            qid = item.get("question_id")
               
            if isinstance(qid, str):
                match = re.search(r"\d+", qid)

                if match:
                    item["question_id"] = int(match.group())

        clean = json.dumps(data)

        result = PerformanceEvaluation.model_validate_json(clean)
        result.reflection_cycles = reflection_cycles

        return result

    except Exception as e:
        raise ValueError(f"PerformanceEvaluator produced invalid JSON: {e}")
