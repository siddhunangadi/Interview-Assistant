"""
agents.py — Specialist agents, each running a genuine ReAct loop.

FLAWS FIXED vs original:
  1. Original used prompt | llm chains — no tool use, no loops, no autonomy.
     Every agent here uses create_react_agent() from langgraph so they can
     Reason → Act (call a tool) → Observe the result → Reason again.

  2. Original had no retry / self-reflection.  Each agent here has a
     _reflect_and_validate() step: the agent's output is scored by the LLM
     itself; if the score is below a threshold the agent re-runs with the
     critique injected, up to MAX_RETRIES times.

  3. Original hard-coded the Mistral API key as a plaintext fallback string
     directly in source code — a serious security flaw.  That is removed.
     The key must come from the environment or the Streamlit sidebar only.

  4. Original defined Agent 6 (performance evaluator) but labelled it in the
     docstring as "Agent 6" while only defining agents 1-4 plus 6, skipping
     a "Agent 5" entirely.  The numbering is now consistent and a proper
     mock-interview facilitator agent (step 5) is added.
"""

