REFLECT_THRESHOLD = 6   # score out of 10 below which agent re-runs


# Helper to coerce dictionary fields generated by LLMs to standard strings
def _coerce_to_string(item: Any) -> str:
    if isinstance(item, str):
        return item
    if isinstance(item, dict):
        parts = []
        for key, val in item.items():
            if val is not None:
                if len(item) == 1:
                    parts.append(str(val))
                else:
                    parts.append(f"{key}: {val}")
        return ", ".join(parts) if parts else str(item)
    return str(item)


class ResumeAnalysis(BaseModel):
    candidate_name: str = Field(description="Full name of the candidate")
    email: Optional[str] = Field(description="Email address of the candidate")
    education: List[str] = Field(description="List of educational qualifications")
    experience_years: float = Field(description="Estimated total professional experience in years")
    skills: List[str] = Field(description="Key technical and soft skills identified")
    projects: List[str] = Field(description="Summary of key projects or achievements")
    strengths: List[str] = Field(description="Top 3-4 candidate strengths")
    weaknesses: List[str] = Field(description="Potential areas of weakness or gaps")

    @model_validator(mode="before")
    @classmethod
    def coerce_fields(cls, data: Any) -> Any:
        if isinstance(data, dict):
            for field in ["education", "skills", "projects", "strengths", "weaknesses"]:
                if field in data and isinstance(data[field], list):
                    data[field] = [_coerce_to_string(x) for x in data[field]]
        return data


